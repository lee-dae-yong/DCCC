package com.DCCC;

public class ProgramSearchCriteria {
	private String searchType;
	private String prg_day;
	private String prg_kind;
	private String prg_time;

	
	public String getSearchType() {
		return searchType;
	}
	public void setSearchType(String searchType) {
		this.searchType = searchType;
	}
	public String getPrg_day() {
		return prg_day;
	}
	public void setPrg_day(String prg_day) {
		this.prg_day = prg_day;
	}
	public String  getPrg_kind() {
		return prg_kind;
	}
	public void setPrg_kind(String prg_kind) {
		this.prg_kind = prg_kind;
	}
	public String getPrg_time() {
		return prg_time;
	}
	public void setPrg_time(String prg_time) {
		this.prg_time = prg_time;
	}
	
	
}
